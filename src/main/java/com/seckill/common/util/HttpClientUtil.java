	/**
	 * ssl发送xml格式的HTTP POST请求
	 * @param httpClient 已经初始化的HttpClient
	 * @param requestConfig 请求配置
	 * @param url 请求地址
	 * @param xml 请求的xml数据
	 * @return 服务端返回的数据字符串
	 */
	public static String doSSLPostXml(String url, String xml, 
			CloseableHttpClient httpClient,RequestConfig requestConfig) {
		CloseableHttpResponse response = null;
		String resultString = "";
		try {
			// 创建Http Post请求
			HttpPost httpPost = new HttpPost(url);
			// 创建请求内容
			StringEntity entity = new StringEntity(xml, ContentType.APPLICATION_XML);
			httpPost.setEntity(entity);
			httpPost.setConfig(requestConfig);
			// 执行http请求
			response = httpClient.execute(httpPost);
			resultString = EntityUtils.toString(response.getEntity(), "utf-8");
		} catch (Exception e) {
			e.printStackTrace();
		} finally {
			try {
				response.close();
			} catch (IOException e) {
				e.printStackTrace();
			}
		}
		return resultString;
	}
